// newTestServer spins up an httptest server with the supplied handler and
// returns a Client pointed at it. The Client is configured with a short
// timeout so a hung test doesn't block CI for minutes.
func newTestServer(t *testing.T, h http.HandlerFunc) (*client.Client, *httptest.Server) {
	t.Helper()
	srv := httptest.NewServer(h)
	t.Cleanup(srv.Close)
	c, err := client.New(srv.URL,
		client.WithAPIKey("kumo_sk_test"),
		client.WithHTTPClient(&http.Client{Timeout: 5 * time.Second}),
	)
	if err != nil {
		t.Fatalf("client.New: %v", err)
	}
	return c, srv
}

// writeStruct is a tiny helper for handlers to emit a StructureResponse.
func writeStruct(w http.ResponseWriter, status int, code, msg string, data any) {
	body := map[string]any{"message": msg}
	if code != "" {
		body["code"] = code
	}
	if data != nil {
		raw, _ := json.Marshal(data)
		body["data"] = json.RawMessage(raw)
	}
	w.Header().Set("Content-Type", "application/json")
	w.WriteHeader(status)
	_ = json.NewEncoder(w).Encode(body)
}

