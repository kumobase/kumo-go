func TestApps_CRUDSmoke(t *testing.T) {
	var seenPath, seenMethod string
	c, _ := newTestServer(t, func(w http.ResponseWriter, r *http.Request) {
		seenPath = r.URL.Path
		seenMethod = r.Method
		switch r.Method {
		case "POST":
			writeStruct(w, 202, "", "queued", &types.CreateAppResponse{ID: 42, Name: "demo"})
		case "GET":
			writeStruct(w, 200, "", "ok", &types.AppByIdResponse{Id: 42})
		case "PATCH":
			writeStruct(w, 202, "", "queued", nil)
		case "DELETE":
			writeStruct(w, 202, "", "queued", nil)
		default:
			http.Error(w, "unexpected method", 500)
		}
	})
	ctx := context.Background()

	created, err := c.Apps().Create(ctx, &types.CreateAppRequest{})
	if err != nil {
		t.Fatalf("Create: %v", err)
	}
	if created.ID != 42 {
		t.Errorf("Create ID: got %d, want 42", created.ID)
	}

	app, _, err := c.Apps().Get(ctx, 42)
	if err != nil {
		t.Fatalf("Get: %v", err)
	}
	if app.Id != 42 {
		t.Errorf("Get Id: got %d, want 42", app.Id)
	}

	if err := c.Apps().Update(ctx, 42, &types.UpdateAppRequest{}); err != nil {
		t.Fatalf("Update: %v", err)
	}
	if seenPath != "/api/v1/apps/42" || seenMethod != "PATCH" {
		t.Errorf("last call: %s %s, want PATCH /api/v1/apps/42", seenMethod, seenPath)
	}

	if err := c.Apps().Delete(ctx, 42); err != nil {
		t.Fatalf("Delete: %v", err)
	}
}

