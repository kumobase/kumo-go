// ── Concurrent safety ────────────────────────────────────────────────

func TestConcurrent_EachCallHasDistinctIdempotencyKey(t *testing.T) {
	var mu sync.Mutex
	keys := map[string]struct{}{}
	c, _ := newTestServer(t, func(w http.ResponseWriter, r *http.Request) {
		key := r.Header.Get("Idempotency-Key")
		mu.Lock()
		keys[key] = struct{}{}
		mu.Unlock()
		writeStruct(w, 202, "", "queued", &types.CreateAppResponse{ID: 1})
	})
	var wg sync.WaitGroup
	const N = 10
	for i := 0; i < N; i++ {
		wg.Add(1)
		go func() {
			defer wg.Done()
			_, _ = c.Apps().Create(context.Background(), &types.CreateAppRequest{})
		}()
	}
	wg.Wait()
	mu.Lock()
	defer mu.Unlock()
	if len(keys) != N {
		t.Errorf("expected %d distinct keys, got %d (%v)", N, len(keys), keys)
	}
}

