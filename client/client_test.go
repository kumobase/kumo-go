func TestRetry_429HonorsRetryAfter(t *testing.T) {
	var attempt atomic.Int32
	var firstSeen, secondSeen time.Time
	c, _ := newTestServer(t, func(w http.ResponseWriter, r *http.Request) {
		a := attempt.Add(1)
		if a == 1 {
			firstSeen = time.Now()
			w.Header().Set("Retry-After", "1")
			writeStruct(w, 429, "", "slow down", nil)
			return
		}
		secondSeen = time.Now()
		writeStruct(w, 200, "", "ok", &types.AppByIdResponse{Id: 1})
	})
	_, _, err := c.Apps().Get(context.Background(), 1)
	if err != nil {
		t.Fatalf("Get: %v", err)
	}
	gap := secondSeen.Sub(firstSeen)
	if gap < 900*time.Millisecond {
		t.Errorf("expected ~1s gap honoring Retry-After, got %v", gap)
	}
}

