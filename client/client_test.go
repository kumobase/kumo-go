func TestIdempotency_HeldConstantAcrossRetries(t *testing.T) {
	var keys []string
	var mu sync.Mutex
	var attempt atomic.Int32
	c, _ := newTestServer(t, func(w http.ResponseWriter, r *http.Request) {
		mu.Lock()
		keys = append(keys, r.Header.Get("Idempotency-Key"))
		mu.Unlock()
		a := attempt.Add(1)
		if a < 3 {
			writeStruct(w, 503, "", "transient upstream", nil)
			return
		}
		writeStruct(w, 202, "", "queued", &types.CreateAppResponse{ID: 1})
	})
	_, err := c.Apps().Create(context.Background(), &types.CreateAppRequest{})
	if err != nil {
		t.Fatalf("Create after retries: %v", err)
	}
	if len(keys) != 3 {
		t.Fatalf("expected 3 attempts, got %d", len(keys))
	}
	if keys[0] != keys[1] || keys[1] != keys[2] {
		t.Errorf("Idempotency-Key drifted across retries: %v", keys)
	}
}

